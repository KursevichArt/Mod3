using System;
using static System.Console;

namespace Mod3_2
{
    public class Уведомление
    {
        // События для разных типов уведомлений
        public event EventHandler ОтправитьСообщение;
        public event EventHandler ОтправитьЗвонок;
        public event EventHandler ОтправитьЭлектронноеПисьмо;

        // Метод для вызова события отправки сообщения
        public void СообщениеОтправлено()
        {
            WriteLine("Сообщение отправлено.");
            ОтправитьСообщение?.Invoke(this, EventArgs.Empty);
        }

        // Метод для вызова события отправки звонка
        public void ЗвонокОтправлен()
        {
            WriteLine("Звонок сделан.");
            ОтправитьЗвонок?.Invoke(this, EventArgs.Empty);
        }

        // Метод для вызова события отправки электронного письма
        public void ПисьмоОтправлено()
        {
            WriteLine("Электронное письмо отправлено.");
            ОтправитьЭлектронноеПисьмо?.Invoke(this, EventArgs.Empty);
        }
    }

    // Пример обработчиков событий
    public class Обработчики
    {
        public void ОбработатьСообщение(object sender, EventArgs e)
        {
            WriteLine("Обработчик: Сообщение доставлено.");
        }

        public void ОбработатьЗвонок(object sender, EventArgs e)
        {
            WriteLine("Обработчик: Звонок принят.");
        }
        public void ОбработатьПисьмо(object sender, EventArgs e)
        {
            WriteLine("Обработчик: Электронное письмо получено.");
        }
    }

    // Пример использования системы событий
    public class Программа
    {
        public static void Main(string[] args)
        {
            Уведомление уведомление = new Уведомление();
            Обработчики обработчики = new Обработчики();

            // Регистрация обработчиков событий
            уведомление.ОтправитьСообщение += обработчики.ОбработатьСообщение;
            уведомление.ОтправитьЗвонок += обработчики.ОбработатьЗвонок;
            уведомление.ОтправитьЭлектронноеПисьмо += обработчики.ОбработатьПисьмо;

            // Вызов событий
            уведомление.СообщениеОтправлено();
            уведомление.ЗвонокОтправлен();
            уведомление.ПисьмоОтправлено();
        }
    }
}
